            String interest = jTextField_taxP.getText();
            double interest1 = Double.parseDouble(interest);

            String ni = jTextField_num_of_inst.getText();
            double ni1 = Double.parseDouble(ni);

            double balance = price1 - d_pay1;

            double tax1 = (balance / 100) * interest1;
            String tax = String.valueOf(tax1);
            jTextField_tax.setText(tax);

            double amount1 = balance + tax1;
            String amount = String.valueOf(amount1);
            jTextField_amount.setText(amount);

            double mi1;
            mi1 = amount1 / ni1;
            String mi = String.valueOf(mi1);
            jTextField_m_inst.setText(mi);

            //==========================================================================================
            //   String xxx = rs.getString("price");
